#include "utils.h"

int		ft_strequ(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	if (*s1 != *s2)
		return (0);
	return (1);
}
